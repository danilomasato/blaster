                <p class="noMargin"> 
                    <strong>Medidor turbina BLLC</strong> indicado para indústria de usina de Açúcar e Álcool, suas aplicações estão nas mais diversas areas como: cosmética, alimentícias, petroquímica, fornos indústria e etc. Nas funções de <a href="https://www.blastercontroles.com.br/indicador-e-totalizador-de-vazao">indicação e totalização de vazão</a>, e pode ser usada para processos de batelada ou dosagem de fluídos não particulados. Quando particulado utiliza-se o <a href="https://www.blastercontroles.com.br/medidor-de-vazao-tipo-eletromagnetico" >medidor tipo eletromagnético</a>. Dúvidas consulte nossa equipe técnica.
                </p> 
                <p class="noMargin">O funcionamento do medidor turbina consiste <em>na medição volumétrica de líquidos e gases</em> limpos, da classe geradora de pulsos, que extrai energia da vazão medida. A <strong>turbina</strong> é largamente usada por causa de seu comprovado excelente desempenho, obtido a partir de altíssimas precisão, linearidade e repetibilidade.</p>

                <p class="noMargin">
                A precisão da turbina é melhor que a de muitos outros medidores de vazão em regime turbulento e é usada como padrão para a calibração e aferição de outros medidores.  A medição com sucesso e precisão da vazão com uma turbina depende de vários fatores. Inicialmente deve se selecionar o medidor e o equipamento condicionador de sinal corretos. </p>

                <p class="noMargin">
                    A seleção é função de  faixa da vazão rangeabilidade temperatura pressão várias propriedades do fluido (densidade,  viscosidade, capacidade de lubrificação, compatibilidade química com o material das partes molhadas do medidor) Partículas contaminantes e sujeiras em suspensão influem na precisão da medição e na sobrevivência da turbina. 
                </p>
                <p class="noMargin">  
                    A seleção dos circuitos eletrônicos associados depende do ambiente, da informação desejada e do tamanho, rangeabilidade e linearidade do medidor. Uma vez todos os componentes do sistema tenham  sido selecionados corretamente, eles devem ser calibrados de modo que a sua medição seja válida.
                </p>
                <p class="noMargin">
                 A viscosidade do líquido e a densidade do gás são muito importantes neste ponto. Por exemplo, uma <strong>turbina calibrada em água</strong> não pode possivelmente fazer uma medição precisa de óleo combustível. Muitos usuários fazem medições baseadas em fator de calibração marcado na turbina sem considerar a validade deste fator para o fluido específico que está sendo medido naquele momento.
                </p>
